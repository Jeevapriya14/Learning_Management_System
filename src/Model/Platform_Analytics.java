package Model;

public class Platform_Analytics {
    public String a_id;
    public String ad_id;
    public String revenue;
    public String no_of_enrollments;
    public String no_of_course;

    public Platform_Analytics(String a_id, String ad_id, String revenue, String no_of_enrollments, String no_of_course) {
        this.a_id = a_id;
        this.ad_id = ad_id;
        this.revenue = revenue;
        this.no_of_enrollments = no_of_enrollments;
        this.no_of_course = no_of_course;
    }
}
